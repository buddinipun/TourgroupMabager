					      	Dalada Maligawa houses the Sacred Tooth Relic of the Buddha which is an object of veneration by Buddhists. It is located in the heart of Kandy, the hill capital of Sri Lanka. There is general belief that the blessings of this Relic has protected Sri Lanka for the past 2500 years. The relic shifted with the shifting of kingdoms, from Anuradhapura, Polonnaruwa, Dambadeniya, Yapahuwa, Kurunegala, Kotte, Gampola and finally to Kandy. According to history the Sacred Tooth which is supposed to Buddha’s left canine was brought to Sri Lanka during the reign of King Kithsiri Meghavanna during the period 301 – 320 BC by Prince Danta and Princess Hemamala from the Kingdom of Kalinga, India. From that time onward it was protected and guarded inside a special shrine, in the royal palace and honoured as the palladium of Kings. It was believed that the possessor of the relic had the right to rule the country. The religious ceremonies connected with it have been conducted uninterrupted even after signing of the Kandyan Convention in 1815. It has been placed under the leadership of three chief custodians. The custodians are the most venerable Mahanayakes of the two chapters of Malwatta and Asgiriya and Diyawadana Nilame.The Sacred Tooth Relic was protected and worshipped by the Sinhalese Kings and by the Sinhalese Buddhist people. A lot of rituals are centered around its worship. It was believed that during the time these rituals were neglected or was taken less care of the people would go through hardships.
The Sri Dalada Maligawa where the tooth relic is housed is recognized as a world heritage site. It is visited by Buddhists from all over the world.

