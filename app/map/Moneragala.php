					      Maligawila, or Maligavila is a village in Sri Lanka, approximately 15 kilometers (9.3 mi) 
					      south from Moneragala and 9 kilometers (5.6 mi) from Okkampitiya in the Moneragala District. It is the
					       site of several important archaeological pieces, including a free-standing 11 meters (36 ft 1.1 in) or 12 meters 
					       (39 ft 4.4 in) tall limestone statue of Buddha, claimed in some quarters to be the tallest free-standing statue of Buddha
					        in the world, though the Avukana Buddha Statue is also said to be taller. This Buddha draws many tourists to the region.
					         It also boasts the Avalokitesvara statue, a 10 meters (32 ft 9.7 in) limestone portrait. These statues, which were discovered 
					         in the 1950s and restored between 1989 and 1991, are believed to have been commissioned by the 7th-century prince Agghabodhi.
					         There is additionally a 10th-century historical pillar placed in the 10th year of the rule of Mahinda IV (956-972 AD).
