					      	Sigiriya is located in the North Central part of Sri Lanka and preserves the architectural tradition of the country. It is about 169 Kms. from Colombo and lies between Anuradhapura and Polonnaruwa. It is within the cultural triangle which includes five of the seven world heritage sites in Sri Lanka. History of the town dates back to more than 7000 years. The town is renowned for its Rock Garden and Sanctuary. Sigiriya rock remained as a Royal Citadel for nearly twenty years. This rock fortress was built by King Kashiyapa. This amazing fortress is in the shape of a lion. It is a world heritage and has gained popularity throughout the world by the frescoes on its rock wall and highly developed water system. The rock of Sigiriya is also known as the Lion’s Rock.<br>
Visitors access the palace through a stone staircase that takes them to the lion’s mouth and through it’s throat. However only two massive paws and brick masonry remain now. The paws gives us an idea how gigantic the rest of the structure must have been. An advanced pumping system had been used to fill the two tanks which were used for bathing and drinking. The water had been pumped from a lake at the foot of the rock.<br><br>
A Hand Craft village has been established under the Ministry of Rural Economic Development. Villagers who are engaged in wood craft, brass work, clay work, leather work, reed work and other allied handicrafts have set up stalls there to exhibit and sell their products. There is also an open stage to conduct cultural shows and also a museum run by the National Design Centre. For the benefit of foreign tourists facilities such as Foreign Currency Exchange, Internet Café and Tourist Information Centre have been set up.<br>

