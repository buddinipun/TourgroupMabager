					   The Jathika Namal Uyana lies in the dry zone, about 159 Km. from Colombo along the Dambulla-Anuradhapura road. It is 7 Km. to the west of Madatugama junction. Archaeological ruins, pink quartz deposits are found here. It was a habitat for several animal and plant species.
Jathika Nmal Uyana is unique in the sense it is supposed to be the oldest man made forest in Sri Lanka. In the 8th century AD King Dappula set up this forest of 238 acre extent and planted with Iron wood trees. It is a home for a variety of animal, bird and plant life. Rare species of lizards and medicinal plants are also found here. The seven summits of Pink Quartz mountain formed 550 million years ago tower above the forest. The mountain stands at a height of about 665 mt. It is considered as the largest concentration of pink quartz in Southeast Asia and the only one found in this country.
As far back as the first century AD and during the reign of King Devanampiyatissa Namal Uyana had been used as a monastery. But during the reign of King Depulu IV by the eighth century it had been transformed as a sanctuary and acted as a refugee centre for people who fled from persecution. The existence of Namal Uyana and the sanctuary have been confirmed by several stone inscriptions in the Anuradhapura area.  These details also appear in one of the two octagonal stone inscription. The Ceylon Tourist Board has initiated the construction of a conservation centre for the protection of Namal Uyana. This will help the visitors to climb the pink quartz mountain as well as meet the villagers living around the Uyana. The guides for the visitors would be provided from the village and Ulpothagama.
