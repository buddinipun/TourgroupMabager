					      	Dambulla Rock Temple is considered as the most impressive cave temple in Sri Lanka. It dates back to the 1st century B.C. It consists of five caves under a overhanging rock. To keep the interiors of the caves dry it is carved with a drip line. The ceilings are adorned with paintings of religious images and various gods and goddesses. Images of Buddha and bodhisattvas are also found here. The caves in the temple have been converted into shrine rooms. The caves stand on 150 mt. high rock and they were built during the periods of Anuradhapura and Polonnaruwa Kingdoms. This famous temple is a UNESCO World Heritage Site. The Dambulla cave is considered as the best-preserved edifice in Sri Lanka.<br><br>
Of the five caves, the first cave is called Devarajalena and is also known as the Cave of the Divine King. The founding of the cave is recorded by the first century Brahmi inscription over the entrance to the first cave. In the second cave, which is the largest cave, there are sixteen standing and forty seated statues of Buddha. There is also the statues of God Saman and Vishnu. The other notable statues are of King Vatta Gamini and King Nissanka Malla found in ‘Maharajalena’ cave. ‘Maha Aluth Viharaya’ (Great New Monastry) is the third cave. A statue of King Keerthi Sri Rajasingha who ruled Kandy from 1747 – 1781. The other two caves are small and not very significiant.<br><br>

